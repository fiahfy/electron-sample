<script>
import { mapActions, mapGetters, mapMutations, mapState } from 'vuex'
import VirtualDataTable from './VirtualDataTable'
import * as ContextMenu from '../utils/context-menu'

export default {
  components: {
    VirtualDataTable
  },
  data () {
    return {
      headers: [
        {
          text: 'Name',
          value: 'name'
        },
        {
          text: 'Size',
          value: 'size',
          width: 64
        },
        {
          text: 'Date Modified',
          value: 'mtime',
          width: 128
        },
        {
          text: 'Bookmark Added',
          value: 'createdAt',
          width: 128
        }
      ]
    }
  },
  computed: {
    ...mapState({
      selectedFilepath: state => state.bookmark.selectedFilepath,
      scrollTop: state => state.bookmark.scrollTop,
      sortOption: state => state.bookmark.sortOption
    }),
    ...mapGetters({
      files: 'bookmark/filteredFiles',
      selectedIndex: 'bookmark/selectedIndex',
      isSelected: 'bookmark/isSelected'
    })
  },
  watch: {
    selectedFilepath () {
      this.$nextTick(() => {
        const index = this.selectedIndex
        if (index === -1) {
          return
        }
        const rowHeight = 48
        const headerHeight = 58
        const el = {
          offsetTop: rowHeight * (index + 1),
          offsetHeight: rowHeight
        }
        const table = {
          scrollTop: this.$refs.table.getScrollTop(),
          offsetHeight: this.$refs.table.getOffsetHeight()
        }
        if (el.offsetTop - el.offsetHeight < table.scrollTop) {
          this.$refs.table.setScrollTop(el.offsetTop - el.offsetHeight)
        } else if (el.offsetTop + headerHeight > table.scrollTop + table.offsetHeight) {
          this.$refs.table.setScrollTop(el.offsetTop + headerHeight - table.offsetHeight)
        }
      })
    }
  },
  mounted () {
    this.restore()
  },
  methods: {
    getHeaderClass (header) {
      return [
        'column sortable',
        this.sortOption.descending ? 'desc' : 'asc',
        header.value === this.sortOption.key ? 'active' : ''
      ]
    },
    getHeaderStyle (header) {
      return {
        'box-sizing': 'content-box',
        width: header.width ? `${header.width}px` : null
      }
    },
    getIcon (file) {
      return file.directory ? 'folder' : 'photo'
    },
    getColor (file) {
      return file.directory ? 'blue lighten-3' : 'green lighten-3'
    },
    getSize (file) {
      return file.directory ? null : file.size
    },
    restore () {
      const scrollTop = this.scrollTop
      this.$nextTick(() => {
        this.$refs.table.setScrollTop(scrollTop)
      })
    },
    changeSort (header) {
      this.changeSortKey({ sortKey: header.value })
      this.$nextTick(() => {
        this.$el.scrollTop = 0
      })
    },
    onScroll (e) {
      this.setScrollTop({ scrollTop: e.target.scrollTop })
    },
    onKeyDown (e) {
      switch (e.keyCode) {
        case 13:
          e.preventDefault()
          this.showViewer({ filepath: this.selectedFilepath })
          break
        case 38:
          e.preventDefault()
          if ((e.ctrlKey && !e.metaKey) || (!e.ctrlKey && e.metaKey)) {
            this.selectFirst()
          } else {
            this.selectPrevious()
          }
          break
        case 40:
          e.preventDefault()
          if ((e.ctrlKey && !e.metaKey) || (!e.ctrlKey && e.metaKey)) {
            this.selectLast()
          } else {
            this.selectNext()
          }
          break
        case 68:
          if ((e.ctrlKey && !e.metaKey) || (!e.ctrlKey && e.metaKey)) {
            e.preventDefault()
            this.toggleBookmark({ filepath: this.selectedFilepath })
          }
          break
      }
    },
    contextmenu (e, file) {
      this.select({ filepath: file.path })
      ContextMenu.show(e, [
        {
          label: 'Bookmark',
          click: () => {
            this.toggleBookmark({ filepath: file.path })
          },
          accelerator: 'CmdOrCtrl+D'
        },
        {
          label: 'View',
          click: () => {
            this.showViewer({ filepath: file.path })
          },
          accelerator: 'Enter'
        },
        { type: 'separator' },
        { role: ContextMenu.Role.copy }
      ])
    },
    ...mapMutations({
      setScrollTop: 'bookmark/setScrollTop'
    }),
    ...mapActions({
      toggleBookmark: 'bookmark/toggleBookmark',
      select: 'bookmark/select',
      selectFirst: 'bookmark/selectFirst',
      selectLast: 'bookmark/selectLast',
      selectPrevious: 'bookmark/selectPrevious',
      selectNext: 'bookmark/selectNext',
      changeSortKey: 'bookmark/changeSortKey',
      action: 'bookmark/action',
      showViewer: 'bookmark/showViewer'
    })
  }
}
</script>

