<template>
  <v-toolbar
    class="viewer-toolbar"
    flat
    dense
  >
    <v-btn
      :title="'View previous image'|accelerator('Left')"
      flat
      icon
      @click="onPreviousClick"
    >
      <v-icon>skip_previous</v-icon>
    </v-btn>
    <v-btn
      :title="'View next image'|accelerator('Right')"
      flat
      icon
      @click="onNextClick"
    >
      <v-icon>skip_next</v-icon>
    </v-btn>
    <v-slider
      v-model="page"
      :min="1"
      :max="maxPage"
      hide-details
      class="pt-0 px-3"
    />
    <span class="px-3">{{ page }} / {{ maxPage }}</span>
    <v-menu
      :close-on-content-click="false"
      v-model="menu"
      top
      offset-y
      nudge-top="12"
    >
      <v-btn
        slot="activator"
        title="Zoom"
        flat
        icon
      >
        <v-icon>zoom_in</v-icon>
      </v-btn>
      <v-toolbar
        ref="toolbar"
        flat
        dense
      >
        <v-btn
          :title="'Zoom in'|accelerator('CmdOrCtrl+Plus')"
          flat
          icon
          @click="onZoomInClick"
        >
          <v-icon>zoom_in</v-icon>
        </v-btn>
        <span class="px-3">{{ scale }}%</span>
        <v-btn
          :title="'Zoom out'|accelerator('CmdOrCtrl+-')"
          flat
          icon
          @click="onZoomOutClick"
        >
          <v-icon>zoom_out</v-icon>
        </v-btn>
        <v-btn
          :title="'Reset'|accelerator('CmdOrCtrl+0')"
          flat
          @click="onResetClick"
        >
          Reset
        </v-btn>
      </v-toolbar>
    </v-menu>
    <v-btn
      v-if="fullScreen"
      title="Exit fullscreen"
      flat
      icon
      @click="onExitFullscreenClick"
    >
      <v-icon>fullscreen_exit</v-icon>
    </v-btn>
    <v-btn
      v-else
      title="Fullscreen"
      flat
      icon
      @click="onFullscreenClick"
    >
      <v-icon>fullscreen</v-icon>
    </v-btn>
    <v-btn
      :title="'Close'|accelerator('Esc')"
      flat
      icon
      @click="onCloseClick"
    >
      <v-icon>close</v-icon>
    </v-btn>
  </v-toolbar>
</template>

<script>
import { mapActions, mapState } from 'vuex'

export default {
  data () {
    return {
      hovered: false,
      menu: false
    }
  },
  computed: {
    page: {
      get () {
        return this.$store.getters['viewer/currentIndex'] + 1
      },
      set (value) {
        this.$store.commit('viewer/setCurrentIndex', { currentIndex: value - 1 })
      }
    },
    ...mapState({
      fullScreen: state => state.fullScreen,
      maxPage: state => state.viewer.items.length,
      scale: state => Math.floor(state.viewer.scale * 100)
    })
  },
  methods: {
    onPreviousClick () {
      this.movePrevious()
    },
    onNextClick () {
      this.moveNext()
    },
    onZoomInClick () {
      this.zoomIn()
    },
    onZoomOutClick () {
      this.zoomOut()
    },
    onResetClick () {
      this.resetZoom()
    },
    onExitFullscreenClick () {
      this.leaveFullScreen()
    },
    onFullscreenClick () {
      this.enterFullScreen()
    },
    onCloseClick () {
      this.dismiss()
    },
    hideMenu () {
      this.menu = false
    },
    isHover () {
      return !!(this.$el.querySelector(':hover') || this.$refs.toolbar.$el.querySelector(':hover'))
    },
    ...mapActions({
      enterFullScreen: 'enterFullScreen',
      leaveFullScreen: 'leaveFullScreen',
      dismiss: 'dismissViewer',
      movePrevious: 'viewer/movePrevious',
      moveNext: 'viewer/moveNext',
      zoomIn: 'viewer/zoomIn',
      zoomOut: 'viewer/zoomOut',
      resetZoom: 'viewer/resetZoom'
    })
  }
}
</script>
