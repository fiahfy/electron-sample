<template>
  <v-navigation-drawer
    class="activity-bar"
    permanent
    mini-variant
    mini-variant-width="48"
  >
    <v-layout column fill-height>
      <v-list dense class="py-0">
        <v-list-item-group :value="index" color="primary">
          <v-list-item
            v-for="menu in menus"
            :key="menu.navigator"
            :title="menu.title | accelerator(menu.accelerator)"
            class="py-1"
            @click="() => handleClickItem(menu)"
          >
            <v-list-item-icon>
              <v-icon v-text="menu.icon" />
            </v-list-item-icon>

            <v-list-item-content>
              <v-list-item-title v-text="menu.title" />
            </v-list-item-content>
          </v-list-item>
        </v-list-item-group>
      </v-list>
      <v-spacer />
      <v-list dense class="py-0">
        <v-list-item
          :title="'Settings' | accelerator('CmdOrCtrl+,')"
          class="py-1"
          @click="handleClickSettings"
        >
          <v-list-item-icon>
            <v-icon>mdi-cog</v-icon>
          </v-list-item-icon>

          <v-list-item-content>
            <v-list-item-title>Settings</v-list-item-title>
          </v-list-item-content>
        </v-list-item>
      </v-list>
    </v-layout>
  </v-navigation-drawer>
</template>

<script lang="ts">
import { defineComponent, computed, SetupContext } from '@vue/composition-api'

type Menu = {
  navigator: string
  icon: string
  title: string
  accelerator: string
}

const menus: Menu[] = [
  {
    navigator: 'files',
    icon: 'mdi-file-multiple',
    title: 'Files',
    accelerator: '',
  },
]

type Props = {
  navigator?: string
}

export default defineComponent({
  props: {
    navigator: {
      type: String,
    },
  },
  setup(props: Props, context: SetupContext) {
    const index = computed(() => {
      return menus.findIndex((menu) => menu.navigator === props.navigator)
    })

    const handleClickItem = (menu: Menu) => {
      context.emit('click-menu', menu)
    }

    const handleClickSettings = () => {
      context.root.$eventBus.$emit('show-settings')
    }

    return {
      menus,
      index,
      handleClickItem,
      handleClickSettings,
    }
  },
})
</script>
