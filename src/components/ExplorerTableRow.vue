<template>
  <tr class="explorer-table-row">
    <td>
      <v-layout class="align-center">
        <v-icon :color="iconColor" class="pa-1">
          {{ icon }}
        </v-icon>
        <span :title="item.name" class="text-truncate spacer">
          {{ item.name }}
        </span>
        <span
          v-if="state.images !== ''"
          class="images text-xs-right caption ml-3"
        >
          {{ state.images }} images
        </span>
      </v-layout>
    </td>
    <td class="text-xs-right">{{ item.views || '' }}</td>
    <td @click.stop @dblclick.stop>
      <v-rating v-model="rating" half-increments clearable />
    </td>
    <td class="no-wrap">
      <template v-if="item.modified_at">
        {{ item.modified_at | date('YYYY-MM-DD HH:mm') }}
      </template>
    </td>
  </tr>
</template>

<script lang="ts">
import {
  defineComponent,
  reactive,
  computed,
  SetupContext,
} from '@vue/composition-api'
import { File } from '~/models'
import { settingsStore } from '~/store'

const workerPromisify = require('@fiahfy/worker-promisify').default
const Worker = require('~/workers/fetch-pathes.worker')

const worker = workerPromisify(new Worker())

type Props = {
  item: File
}

export default defineComponent({
  props: {
    item: {
      type: Object,
      default: () => ({}),
    },
  },
  setup(props: Props, context: SetupContext) {
    const state = reactive({
      loading: false,
      error: false,
      images: '',
    })

    const rating = computed({
      get() {
        return props.item.rating
      },
      set(value) {
        context.emit('change-rating', Number(value))
      },
    })
    const icon = computed(() => {
      if (props.item.exists) {
        return props.item.directory ? 'mdi-folder' : 'mdi-image'
      }
      return 'mdi-image-broken-variant'
    })
    const iconColor = computed(() => {
      if (props.item.exists) {
        return props.item.directory ? 'blue lighten-3' : 'green lighten-3'
      }
      return 'grey'
    })

    const load = async () => {
      if (!props.item.directory) {
        return
      }
      state.loading = true
      const { data } = await worker.postMessage({
        key: props.item.path,
        path: props.item.path,
      })
      const filePathes = data.filter((filePath: string) =>
        settingsStore.isFileAvailable({ filePath })
      )
      state.images = filePathes.length
      state.loading = false
    }

    load()

    return {
      state,
      rating,
      icon,
      iconColor,
    }
  },
})
</script>

<style lang="scss" scoped>
.explorer-table-row {
  cursor: pointer;
  td {
    .v-icon {
      user-select: none;
    }
    .v-rating {
      white-space: nowrap;
    }
    .images {
      white-space: nowrap;
    }
  }
}
</style>
