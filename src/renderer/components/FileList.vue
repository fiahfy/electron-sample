<template>
  <mdc-table
    class="file-list"
    tabindex="0"
    @keydown.native="keydown"
  >
    <mdc-table-header>
      <mdc-table-row>
        <mdc-table-header-column
          class="name mdc-theme--background"
          @click.native="changeSortKey({ key: 'name' })"
        >
          <span>Name</span>
          <mdc-icon
            :icon="sortIcon"
            v-if="sortKey === 'name'"
          />
        </mdc-table-header-column>
        <mdc-table-header-column
          class="size mdc-theme--background"
          @click.native="changeSortKey({ key: 'size' })"
        >
          <span>Size</span>
          <mdc-icon
            :icon="sortIcon"
            v-if="sortKey === 'size'"
          />
        </mdc-table-header-column>
        <mdc-table-header-column
          class="date-modified mdc-theme--background"
          @click.native="changeSortKey({ key: 'date_modified' })"
        >
          <span>Date Modified</span>
          <mdc-icon
            :icon="sortIcon"
            v-if="sortKey === 'date_modified'"
          />
        </mdc-table-header-column>
      </mdc-table-row>
    </mdc-table-header>

    <mdc-virtual-table-body
      :items="files"
      :estimatedHeight="41"
      v-if="improveRenderingPerformance"
    >
      <file-list-item
        slot-scope="{ item }"
        :key="item.name"
        :file="item"
        :class="{ selected: isSelected(item) }"
        @click.native="selectFile({ file: item })"
        @dblclick.native="doubleClick(item)"
      />
    </mdc-virtual-table-body>
    <mdc-table-body v-else>
      <file-list-item
        :key="item.name"
        :file="item"
        :class="{ selected: isSelected(item) }"
        @click.native="selectFile({ file: item })"
        @dblclick.native="doubleClick(item)"
        v-for="item in files"
      />
    </mdc-table-body>

  </mdc-table>
</template>

<script>
import { mapActions, mapState } from 'vuex'
import FileListItem from '../components/FileListItem'
import MdcIcon from '../components/MdcIcon'
import MdcTable from '../components/MdcTable'
import MdcTableBody from '../components/MdcTableBody'
import MdcTableHeader from '../components/MdcTableHeader'
import MdcTableHeaderColumn from '../components/MdcTableHeaderColumn'
import MdcTableRow from '../components/MdcTableRow'
import MdcVirtualTableBody from '../components/MdcVirtualTableBody'

export default {
  components: {
    FileListItem,
    MdcIcon,
    MdcTable,
    MdcTableBody,
    MdcTableHeader,
    MdcTableHeaderColumn,
    MdcTableRow,
    MdcVirtualTableBody
  },
  computed: {
    sortIcon () {
      return this.sortOrder === 'asc' ? 'arrow_drop_up' : 'arrow_drop_down'
    },
    ...mapState('explorer', [
      'directory',
      'files',
      'selectedFile',
      'sortKey',
      'sortOrder'
    ]),
    ...mapState('settings', [
      'improveRenderingPerformance'
    ])
  },
  methods: {
    isSelected (file) {
      return this.selectedFile && file.path === this.selectedFile.path
    },
    doubleClick (file) {
      this.action({ filepath: file.path })
    },
    keydown (e) {
      if ((e.ctrlKey && !e.metaKey) || (!e.ctrlKey && e.metaKey)) {
        return
      }
      switch (e.keyCode) {
        case 13:
          e.preventDefault()
          this.showSelectedFile()
          break
        case 37:
          e.preventDefault()
          this.changeParentDirectory()
          break
        case 38:
          e.preventDefault()
          this.selectPreviousFile()
          break
        case 39:
          e.preventDefault()
          this.changeSelectedDirectory()
          break
        case 40:
          e.preventDefault()
          this.selectNextFile()
          break
      }
    },
    scroll () {
      this.$nextTick(() => {
        const parent = this.$el.parentNode
        if (!parent) {
          return
        }
        const index = this.files.findIndex(this.isSelected) || 0
        const rowHeight = 41
        const offsetHeight = 41
        const el = {
          offsetTop: rowHeight * index + offsetHeight,
          offsetHeight: rowHeight
        }
        if (el.offsetTop - el.offsetHeight < parent.scrollTop) {
          parent.scrollTop = el.offsetTop - el.offsetHeight
        } else if (el.offsetTop + el.offsetHeight > parent.scrollTop + parent.offsetHeight) {
          parent.scrollTop = el.offsetTop + el.offsetHeight - parent.offsetHeight
        }
      })
    },
    ...mapActions('explorer', [
      'changeParentDirectory',
      'changeSelectedDirectory',
      'changeSortKey',
      'selectFile',
      'selectPreviousFile',
      'selectNextFile',
      'action'
    ]),
    ...mapActions('viewer', [
      'showSelectedFile'
    ])
  },
  watch: {
    directory () {
      this.$el.parentNode.scrollTop = 0
    },
    files () {
      this.scroll()
    },
    selectedFile () {
      this.scroll()
    }
  }
}
</script>

